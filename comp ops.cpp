#include <iostream>
using namespace std;
int main() {
    int a = 4, b = 2;
    
    //0 = false, 1 = true.
    cout << "The value of a == b is: " << (a==b) << endl;
    cout << "The value of a != b is: " << (a!=b) << endl;
    cout << "The value of a > b is: " << (a>b) << endl;
    cout << "The value of a < b is: " << (a<b) << endl;
    cout << "The value of a >= b is: " << (a>=b) << endl;
    cout << "The value of a <= b is: " << (a<=b) << endl;
  
    return 0;
}